//java program for 'nested for loop'
public class NestedForLoop{
    public static void main(String a[]){
        //loop of x
        for(int x=1;x<=4;x++){
            //loop of y
            for(int y=1;y<=4;y++){
              //print values of 'x' and 'y' on the same line then arrange  
                System.out.println(x+"  "+y);
            }//end of y
        }//end of x
    }
}